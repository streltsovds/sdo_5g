    private static function _constructMap()
    {
        if (is_array(self::$_extensionToMime)) {
            return;
        }
    
        self::$_extensionToMime = array();
        
        foreach (self::$_defaultMimeTypes as $mime => $extensions) {
            foreach ($extensions as $extension) {
	            self::$_extensionToMime[strtolower($extension)][] = $mime;
            }
        }
    }

    public static function getExtensionsArray($extensions)
    {
        if (is_string($extensions)) {
            $extensions = preg_split("/\s*[;,]\s*/", trim($extensions));
        }
        if (!is_array($extensions)) {
            $extensions = array();
        }
        $tmpExtensions = array();
        foreach ($extensions as $key => $value) {
            $tmp = trim(preg_replace("/^\*?\.?/", "", $value));
            if ($tmp && !empty($tmp) && $tmp != '') {
                $tmpExtensions[] = preg_replace("/^\*?\.?/", "", $value);
            }
        }
        return $tmpExtensions;
    }

    public static function extensionsToMime($extensions)
    {
        self::_constructMap();
        $extensions = self::getExtensionsArray($extensions);
        $result = array();

        foreach ($extensions as $extension) {
            $extension_lc = strtolower($extension);
            if (isset(self::$_extensionToMime[$extension_lc])) {
                $result = array_merge($result, self::$_extensionToMime[$extension_lc]);
            } else if ($extension_lc == '*') {
                $result[] = '*/*';
            } else {
                $result[] = 'application/octet-stream';
            }
        }
        return array_unique($result);
    }


